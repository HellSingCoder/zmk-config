/*
    All the animations are composed of 3 frames, however, 4 are displayed in a full loop.
    The frame to display is chosen by bouncing back and forth trough the pointer arrays,
    this means that they will be displayed in the sequence of 0 1 2 1 and then repeat.
    In easier terms, we can consider the first and last frame as end states and the middle as interpolation.
    This allows for smoother animations saving some space.

    Ex. 
        In the case of a walk cycle we will have:

        0 - right step
        1 - standing still
        2 - left step

        right, stand, left, stand, right, stand, left, stand, and so on...

    NOTE: This does not apply to the space animation as I intended it to be a jump that requires 4 different frames.
*/

